    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {                                             
        String nombre = txtNombreCompleto.getText();
        String usuario = txtUsuario.getText();
        String correo = txtCorreo.getText();
        String pass1 = new String(txtContraseña.getPassword());
        String pass2 = new String(txtConfirmarContraseña.getPassword());
        
        // 1. Validaciones lógicas básicas de los campos de texto
        if(nombre.isEmpty() || usuario.isEmpty() || correo.isEmpty() || pass1.isEmpty() || pass2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
        }
        else if(!pass1.equals(pass2)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas deben coincidir");
            txtContraseña.setText("");
            txtConfirmarContraseña.setText("");
        }
        else if(!correo.contains("@") || !correo.contains(".")) {
            JOptionPane.showMessageDialog(this, "Ingresa un correo electrónico válido.");
            txtCorreo.setText("");
        } else {
            
            // 2. Proceso de inserción utilizando tu método getConnection() instanciado
            com.abarrotes.pos.modelo.Conexion herramienta = new com.abarrotes.pos.modelo.Conexion();
            Connection con = herramienta.getConnection();
            
            if (con != null) {
                // Sentencia SQL apuntando exactamente a las columnas de tu tabla
                String sql = "INSERT INTO usuarios (nombre, usuario, correo, contraseña) VALUES (?, ?, ?, ?)";
                
                try (PreparedStatement ps = con.prepareStatement(sql)) {
                    ps.setString(1, nombre);
                    ps.setString(2, usuario);
                    ps.setString(3, correo);
                    ps.setString(4, pass1);

                    int resultado = ps.executeUpdate();

                    if (resultado > 0) {
                        JOptionPane.showMessageDialog(this, "¡Usuario registrado exitosamente en la Base de Datos!");
                        
                        // Redirección limpia al Login al terminar con éxito
                        frmLogin login = new frmLogin();
                        login.setVisible(true);
                        login.setLocationRelativeTo(null);
                        this.dispose();
                    }

                } catch (SQLException e) {
                    // Control de duplicidad en campos UNIQUE como 'usuario'
                    if (e.getErrorCode() == 1062) { 
                        JOptionPane.showMessageDialog(this, "El nombre de usuario ya está registrado.", "Error", JOptionPane.ERROR_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Error en la base de datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } finally {
                    try {
                        con.close();
                    } catch (SQLException ex) {
                        logger.log(java.util.logging.Level.SEVERE, null, ex);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error de comunicación: No se pudo conectar al servidor de Base de Datos.", "Error de conexión", JOptionPane.ERROR_MESSAGE);
            }
        }
    }                                            

