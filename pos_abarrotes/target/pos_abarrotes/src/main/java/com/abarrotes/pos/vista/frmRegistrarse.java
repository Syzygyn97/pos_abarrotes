    private void configurarPermisos() {
        chkInventario.addActionListener(e -> {
            boolean estado = chkInventario.isSelected();
            chkProveedores.setSelected(estado);
            chkProductos.setSelected(estado);
        });
        
        chkVentas.addActionListener(e -> {
            boolean estado = chkVentas.isSelected();
            chkPuntoVenta.setSelected(estado);
            chkCancelarProducto.setSelected(estado);
            chkCancelarVenta.setSelected(estado);
        });

        java.awt.event.ActionListener sincInventario = e -> {
            chkInventario.setSelected(chkProveedores.isSelected() || chkProductos.isSelected());
        };
        java.awt.event.ActionListener sincVentas = e -> {
            chkVentas.setSelected(chkPuntoVenta.isSelected() || chkCancelarProducto.isSelected() || chkCancelarVenta.isSelected());
        };
            
        chkProveedores.addActionListener(sincInventario);
        chkProductos.addActionListener(sincInventario);
        chkPuntoVenta.addActionListener(sincVentas);
        chkCancelarProducto.addActionListener(sincVentas);
        chkCancelarVenta.addActionListener(sincVentas);
    }
    
